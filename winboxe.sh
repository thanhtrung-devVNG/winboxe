case "${1:-}" in
    --stop|-s)
        _stop_dashboard
        ;;
    --status|-st)
        _show_status
        ;;
    --logs|-l)
        if [[ -f "$INSTALL_DIR/dashboard.log" ]]; then
            tail -50 "$INSTALL_DIR/dashboard.log"
        else
            echo "Chua co log nao"
        fi
        ;;
    --help|-h)
        _show_help
        ;;
    *)
        _start_dashboard
        ;;
esac
