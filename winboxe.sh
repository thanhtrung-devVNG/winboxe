_ensure_deps() {
    echo -e "${B}[*]${W} Kiem tra dependencies..."
    if ! command -v python3 &>/dev/null; then
        echo -e "${R}[!]${W} Python3 khong tim thay!"
        exit 1
    fi
    if ! python3 -c "import flask" 2>/dev/null; then
        echo -e "${Y}[*]${W} Dang cai Flask..."
        pip3 install flask --quiet 2>/dev/null ||         python3 -m pip install flask --quiet 2>/dev/null || {
            echo -e "${R}[!]${W} Khong cai duoc Flask. Thu: pip3 install flask"
            exit 1
        }
    fi
    echo -e "${G}[OK]${W} Dependencies OK"
}

_ensure_winbox_script() {
    if [[ ! -f "$WINBOX_SCRIPT" ]]; then
        echo -e "${Y}[!]${W} Khong tim thay $WINBOX_SCRIPT"
        echo -e "${B}[*]${W} Tai ve tu GitHub..."
        local _urls=(
            "https://raw.githubusercontent.com/assassin255/WinBoxes-Source/refs/heads/main/winboxes-stable-3-2.sh"
            "https://raw.githubusercontent.com/assassin255/WinBoxes-Source/main/winboxes-stable-3-2.sh"
        )
        local _ok=0
        for _url in "${_urls[@]}"; do
            if wget -q -O "$WINBOX_SCRIPT" "$_url" 2>/dev/null ||                curl -fsSL -o "$WINBOX_SCRIPT" "$_url" 2>/dev/null; then
                chmod +x "$WINBOX_SCRIPT"
                _ok=1
                break
            fi
        done
        if [[ "$_ok" == "0" ]]; then
            echo -e "${R}[!]${W} Khong tai duoc script goc."
            echo -e "${Y}    Vui long tu tai winbox.sh ve va dat WINBOX_SCRIPT=/duong/dan${W}"
            exit 1
        fi
        echo -e "${G}[OK]${W} Da tai winbox.sh"
    fi
}

_extract_files() {
    mkdir -p "$INSTALL_DIR"
    echo -e "${B}[*]${W} Giai ma files..."

    printf "%s" "$_DASHBOARD_HTML_B64" | base64 -d > "$INSTALL_DIR/dashboard.html" 2>/dev/null ||     python3 -c "import base64,sys; sys.stdout.buffer.write(base64.b64decode(sys.stdin.read().replace(chr(10),'').replace(' ','')))" <<< "$_DASHBOARD_HTML_B64" > "$INSTALL_DIR/dashboard.html"

    printf "%s" "$_PYTHON_B64" | base64 -d > "$INSTALL_DIR/winbox-dashboard.py" 2>/dev/null ||     python3 -c "import base64,sys; sys.stdout.buffer.write(base64.b64decode(sys.stdin.read().replace(chr(10),'').replace(' ','')))" <<< "$_PYTHON_B64" > "$INSTALL_DIR/winbox-dashboard.py"

    sed -i "s|os.path.join(os.path.dirname(__file__), .dashboard.html.)|'$INSTALL_DIR/dashboard.html'|g" "$INSTALL_DIR/winbox-dashboard.py" 2>/dev/null || true

    echo -e "${G}[OK]${W} Files ready"
}

_start_dashboard() {
    if [[ -f "$PID_FILE" ]]; then
        local _old_pid
        _old_pid=$(cat "$PID_FILE" 2>/dev/null || echo "")
        if [[ -n "$_old_pid" ]] && kill -0 "$_old_pid" 2>/dev/null; then
            echo -e "${Y}[!]${W} Dashboard dang chay (PID: $_old_pid)"
            echo -e "${B}    Truy cap: http://localhost:$DASHBOARD_PORT${W}"
            return
        fi
    fi

    _ensure_deps
    _ensure_winbox_script
    _extract_files

    export WINBOX_SCRIPT="$WINBOX_SCRIPT"
    export WINBOX_DASHBOARD_PORT="$DASHBOARD_PORT"

    echo ""
    echo -e "${C}══════════════════════════════════════════════════════════════${W}"
    echo -e "${C}               WINBOX DASHBOARD${W}"
    echo -e "${C}══════════════════════════════════════════════════════════════${W}"
    echo -e "${G}  Truy cap: http://localhost:$DASHBOARD_PORT${W}"
    echo -e "${G}  Script  : $WINBOX_SCRIPT${W}"
    echo -e "${G}  Data    : $INSTALL_DIR${W}"
    echo -e "${C}══════════════════════════════════════════════════════════════${W}"
    echo ""

    nohup python3 "$INSTALL_DIR/winbox-dashboard.py" > "$INSTALL_DIR/dashboard.log" 2>&1 &
    local _pid=$!
    echo "$_pid" > "$PID_FILE"
    disown "$_pid" 2>/dev/null || true

    sleep 2
    if kill -0 "$_pid" 2>/dev/null; then
        echo -e "${G}[OK]${W} Dashboard da khoi dong (PID: $_pid)"
        echo -e "${B}    Mo trinh duyet: http://localhost:$DASHBOARD_PORT${W}"
        echo ""
        echo -e "${Y}  [Luu y] Khi tao VPS tren web, terminal moi se tu dong mo${W}"
        echo -e "${Y}          de hien thi qua trinh chay winbox.sh${W}"
    else
        echo -e "${R}[!]${W} Dashboard khong khoi dong duoc!"
        echo -e "${Y}    Log: $INSTALL_DIR/dashboard.log${W}"
        cat "$INSTALL_DIR/dashboard.log" 2>/dev/null | tail -20 || true
        exit 1
    fi
}

_stop_dashboard() {
    if [[ -f "$PID_FILE" ]]; then
        local _pid
        _pid=$(cat "$PID_FILE" 2>/dev/null || echo "")
        if [[ -n "$_pid" ]] && kill -0 "$_pid" 2>/dev/null; then
            kill "$_pid" 2>/dev/null || true
            sleep 1
            kill -0 "$_pid" 2>/dev/null && kill -9 "$_pid" 2>/dev/null || true
            echo -e "${G}[OK]${W} Dashboard da dung (PID: $_pid)"
        else
            echo -e "${Y}[!]${W} Dashboard khong con chay"
        fi
        rm -f "$PID_FILE"
    else
        echo -e "${Y}[!]${W} Khong tim thay PID file"
    fi
}

_show_status() {
    echo -e "${C}══════════════════════════════════════════════════════════════${W}"
    echo -e "${C}               WINBOX DASHBOARD STATUS${W}"
    echo -e "${C}══════════════════════════════════════════════════════════════${W}"
    if [[ -f "$PID_FILE" ]]; then
        local _pid
        _pid=$(cat "$PID_FILE" 2>/dev/null || echo "")
        if [[ -n "$_pid" ]] && kill -0 "$_pid" 2>/dev/null; then
            echo -e "${G}  Trang thai: DANG CHAY${W}"
            echo -e "${G}  PID       : $_pid${W}"
            echo -e "${G}  URL       : http://localhost:$DASHBOARD_PORT${W}"
        else
            echo -e "${R}  Trang thai: DA DUNG${W}"
        fi
    else
        echo -e "${R}  Trang thai: CHUA CHAY${W}"
    fi
    echo -e "${B}  Script    : $WINBOX_SCRIPT${W}"
    echo -e "${B}  Data      : $INSTALL_DIR${W}"
    echo -e "${C}══════════════════════════════════════════════════════════════${W}"
}

_show_help() {
    cat << 'HELP'
╔══════════════════════════════════════════════════════════════╗
║               WINBOX DASHBOARD — All-in-One                  ║
╠══════════════════════════════════════════════════════════════╣
║  Cach dung:                                                  ║
║    bash winbox-dashboard.sh           # Khoi dong dashboard  ║
║    bash winbox-dashboard.sh --stop    # Dung dashboard       ║
║    bash winbox-dashboard.sh --status  # Xem trang thai       ║
║    bash winbox-dashboard.sh --logs    # Xem logs             ║
╠══════════════════════════════════════════════════════════════╣
║  Bien moi truong:                                            ║
║    WINBOX_DASHBOARD_PORT=8080   # Doi port                   ║
║    WINBOX_SCRIPT=./winbox.sh    # Duong dan script goc       ║
╚══════════════════════════════════════════════════════════════╝
HELP
}

# ════════════════════════════════════════════════════════════════
#  MAIN
# ════════════════════════════════════════════════════════════════
case "${1:-}" in
    --stop|-s)
        _stop_dashboard
        ;;
    --status|-st)
        _show_status
        ;;
    --logs|-l)
        if [[ -f "$INSTALL_DIR/dashboard.log" ]]; then
            tail -50 "$INSTALL_DIR/dashboard.log"
        else
            echo "Chua co log nao"
        fi
        ;;
    --help|-h)
        _show_help
        ;;
    *)
        _start_dashboard
        ;;
esac
